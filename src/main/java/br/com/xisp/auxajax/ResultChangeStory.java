package br.com.xisp.auxajax;

public class ResultChangeStory {
	
	private Long id;
	private String status;
	private Integer qtdStories;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Integer getQtdStories() {
		return qtdStories;
	}
	public void setQtdStories(Integer qtdStories) {
		this.qtdStories = qtdStories;
	}
	
	
	
	

}
